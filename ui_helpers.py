# ── Tooltip helpers ─────────────────────────────────────────────────────────────

def term_tip(label: str) -> str:
    """回傳含 <abbr> tooltip 的 HTML 字串，供嵌入 st.markdown(unsafe_allow_html=True)。
    若術語不在 GLOSSARY，直接回傳原文不加標記。

    用法::

        st.markdown(f"目前 {term_tip('融資餘額')} 偏高…", unsafe_allow_html=True)
    """
    tip = GLOSSARY.get(label, "")
    if not tip:
        return label
    safe = tip.replace('"', "’")  # 雙引號換為右單引號避免 HTML 屬性破壞
    return (
        f'<abbr title="{safe}" style="'
        f"text-decoration:underline dotted 1.5px #888;"
        f"cursor:help;text-underline-offset:3px;font-style:normal"
        f'">{label}</abbr>'
    )


def metric_tip(container, label: str, value, delta=None, tip_key: str | None = None) -> None:
    """st.metric / col.metric 加 GLOSSARY help 文字的封裝。

    用法::

        metric_tip(st, "外資(最新)", "+120 億", tip_key="外資")
        metric_tip(c1, "融資餘額", "3,000 億", delta="+1.2%")
    """
    tip = GLOSSARY.get(tip_key or label, "")
    kwargs: dict = {"label": label, "value": value}
    if delta is not None:
        kwargs["delta"] = delta
    if tip:
        kwargs["help"] = tip
    container.metric(**kwargs)


# ── SPEC 卡片 ───────────────────────────────────────────────────────────────────

def render_spec_card(
    name: str,
    source: str,
    freq: str,
    bull: str,
    bear: str,
    note: str = "",
) -> None:
    """折疊式指標 SPEC 說明卡（📋 label — 指標說明）。

    Args:
        name:   指標名稱，顯示於 expander 標題
        source: 資料來源（例：「證交所 BFI82U，真實數據」）
        freq:   更新頻率（例：「每個交易日盤後 17:00」）
        bull:   看多信號說明
        bear:   看空信號說明
        note:   補充說明（可空）
    """
    with st.expander(f"📋 {name} — 指標說明", expanded=False):
        col1, col2 = st.columns(2)
        with col1:
            st.markdown(f"**📡 資料來源**  \n{source}")
            st.markdown(f"**🔄 更新頻率**  \n{freq}")
        with col2:
            st.markdown(f"**🟢 看多信號**  \n{bull}")
            st.markdown(f"**🔴 看空信號**  \n{bear}")
        if note:
            st.caption(f"💡 {note}")


